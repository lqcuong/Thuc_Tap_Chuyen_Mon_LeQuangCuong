package form.QLPM;

public class Benh {
	String maBenh,loaiBenh,moTaBenh;

	public Benh() {
		super();
	}

	public Benh(String maBenh, String loaiBenh, String moTaBenh) {
		super();
		this.maBenh = maBenh;
		this.loaiBenh = loaiBenh;
		this.moTaBenh = moTaBenh;
	}

	public String getMaBenh() {
		return maBenh;
	}

	public void setMaBenh(String maBenh) {
		this.maBenh = maBenh;
	}

	public String getLoaiBenh() {
		return loaiBenh;
	}

	public void setLoaiBenh(String loaiBenh) {
		this.loaiBenh = loaiBenh;
	}

	public String getMoTaBenh() {
		return moTaBenh;
	}

	public void setMoTaBenh(String moTaBenh) {
		this.moTaBenh = moTaBenh;
	}
	
}
