package form.QLPM;

public class BenhNhanTab2 {
	String hoTen;
	String ngayKham;
	String loaiBenh;
	String trieuChung;
	String maPhieuKham;
	public BenhNhanTab2() {
		super();
	}
	public BenhNhanTab2(String hoTen, String ngayKham, String loaiBenh, String trieuChung, String maPhieuKham) {
		super();
		this.hoTen = hoTen;
		this.ngayKham = ngayKham;
		this.loaiBenh = loaiBenh;
		this.trieuChung = trieuChung;
		this.maPhieuKham = maPhieuKham;
	}
	public String getHoTen() {
		return hoTen;
	}
	public void setHoTen(String hoTen) {
		this.hoTen = hoTen;
	}
	public String getNgayKham() {
		return ngayKham;
	}
	public void setNgayKham(String ngayKham) {
		this.ngayKham = ngayKham;
	}
	public String getLoaiBenh() {
		return loaiBenh;
	}
	public void setLoaiBenh(String loaiBenh) {
		this.loaiBenh = loaiBenh;
	}
	public String getTrieuChung() {
		return trieuChung;
	}
	public void setTrieuChung(String trieuChung) {
		this.trieuChung = trieuChung;
	}
	public String getMaPhieuKham() {
		return maPhieuKham;
	}
	public void setMaPhieuKham(String maPhieuKham) {
		this.maPhieuKham = maPhieuKham;
	}

}
